# Basic Python Data Types
a = 10                   # int
b = 3.14                 # float
c = "Hello"              # str
d = [1, 2, 3]            # list
e = (4, 5, 6)            # tuple
f = {7, 8, 9}            # set
g = {"name": "Razaq"}    # dict
h = True                 # bool
i = None                 # NoneType

# Print type of each variable
print(type(a))
print(type(b))
print(type(c))
print(type(d))
print(type(e))
print(type(f))
print(type(g))
print(type(h))
